-- ====================================================================
-- MOHANOE INC. LEGAL PRACTICE MANAGEMENT - DATABASE SCHEMA
-- ====================================================================

-- --------------------------------------------------------------------
-- TABLE: users
-- Stores user accounts for staff and clients.
-- --------------------------------------------------------------------
CREATE TABLE users (
  id uuid PRIMARY KEY DEFAULT auth.uid(),
  email TEXT UNIQUE NOT NULL,
  full_name TEXT,
  role TEXT NOT NULL DEFAULT 'client',
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- --------------------------------------------------------------------
-- TABLE: clients
-- Stores detailed information about clients.
-- --------------------------------------------------------------------
CREATE TABLE clients (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid REFERENCES users(id) ON DELETE SET NULL, -- Link to the user account if they have one
  first_name TEXT NOT NULL,
  last_name TEXT NOT NULL,
  email TEXT UNIQUE,
  phone_number TEXT,
  address TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- --------------------------------------------------------------------
-- TABLE: cases
-- Stores information about legal cases.
-- --------------------------------------------------------------------
CREATE TABLE cases (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_id BIGINT NOT NULL REFERENCES clients(id) ON DELETE CASCADE,
  case_number TEXT UNIQUE NOT NULL,
  case_description TEXT,
  practice_area TEXT,
  status TEXT DEFAULT 'open', -- e.g., 'open', 'closed', 'pending'
  lead_attorney_id uuid REFERENCES users(id) ON DELETE SET NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- --------------------------------------------------------------------
-- RLS (Row-Level Security) Policies
-- Ensure that users can only access their own data.
-- --------------------------------------------------------------------

-- Enable RLS on all relevant tables
ALTER TABLE clients ENABLE ROW LEVEL SECURITY;
ALTER TABLE cases ENABLE ROW LEVEL SECURITY;

-- Policy: Allow users to see their own client profile
CREATE POLICY "Users can view their own client data" 
ON clients FOR SELECT
USING (auth.uid() = user_id);

-- Policy: Allow staff (e.g., 'attorney', 'admin') to access all clients
CREATE POLICY "Staff can access all client data" 
ON clients FOR ALL
USING ( (SELECT role FROM users WHERE id = auth.uid()) IN ('admin', 'attorney', 'paralegal') );

-- Policy: Allow clients to see their own cases
CREATE POLICY "Clients can view their own cases" 
ON cases FOR SELECT
USING (client_id IN (SELECT id FROM clients WHERE user_id = auth.uid()));

-- Policy: Allow staff to access all cases
CREATE POLICY "Staff can access all cases" 
ON cases FOR ALL
USING ( (SELECT role FROM users WHERE id = auth.uid()) IN ('admin', 'attorney', 'paralegal') );

